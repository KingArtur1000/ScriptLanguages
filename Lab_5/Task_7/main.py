from vars import *
import numpy as np

# Создаем массив 3x4 из случайных чисел нормального распределения
array_3x4 = np.random.normal(loc=0, scale=1.0, size=(3, 4))
print("Массив 3x4 из случайных чисел с нормальным распределением:")
print(array_3x4)

# Преобразуем массив в одномерный с тем же size
array_flattened = array_3x4.ravel()
print("\nОдномерный массив с таким же size:")
print(array_flattened)

'''
• Для генерации случайных чисел из нормального распределения используется np.random.normal. Параметры:
• loc=0: среднее значение.
• scale=1.0: стандартное отклонение.
• size=(3, 4): размер массива.
• Метод .ravel() преобразует массив в одномерный без изменения общего числа элементов (size).
'''
