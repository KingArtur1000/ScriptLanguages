from vars import *
import numpy as np

# Создаем одномерный массив А от 0 до М
A = np.arange(0, M + 1)
print("Одномерный массив A:")
print(A)

# Меняем форму массива А для получения матрицы C (4 строки, 3 столбца)
# Убедимся, что размер массива позволяет преобразование
if A.size >= 12:
    C = A[:12].reshape(4, 3)
    print("\nМатрица C (4 строки, 3 столбца):")
    print(C)
else:
    print("\nНедостаточно элементов в массиве A для создания матрицы 4x3.")
    exit()

# Транспонируем матрицу C
CT = C.T
print("\nТранспонированная матрица C (CT):")
print(CT)

# Умножаем матрицу C на её транспонированную матрицу CT
B = np.dot(C, CT)
print("\nМатрица B (результат матричного умножения C на CT):")
print(B)
